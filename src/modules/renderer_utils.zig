//! 渲染器辅助函数

pub fn boxCharHasUp(u: u21) bool {
    return switch (u) {
        0x2502,
        0x2503,
        0x2506...0x250B,
        0x2510...0x2513,
        0x2514...0x2517, // │, ┐, └
        0x2518...0x251B,
        0x251C...0x251F,
        0x2520...0x2523, // ┘, ├, ┤
        0x2524...0x252B,
        0x252C...0x2533,
        0x2534...0x253B, // ┬, ┴, ┼
        0x253C...0x254B,
        0x2550...0x2551,
        0x2552...0x2554,
        0x2555...0x2557,
        0x2558...0x255A,
        0x255B...0x255D,
        0x255E...0x2560,
        0x2561...0x2563,
        0x2564...0x2566,
        0x2567...0x2569,
        0x256A...0x256C,
        => true,
        else => false,
    };
}

